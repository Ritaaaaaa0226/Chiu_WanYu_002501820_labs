    private void populateTable(){
        
        //Table Variable
        DefaultTableModel model = (DefaultTableModel) tblVital.getModel();
        
        //Intiallize Table
        model.setRowCount(0);
        
        //data put in table and display one by one
        for(VitalSign vs: history.getHistory()){
            
            //Object array for five object data
            Object[] row = new Object[5];
            row[0] = vs;//data is not a object but a stringï¼Œso use override
            row[1] = vs.getTemperature();
            row[2] = vs.getBloodpressure();
            row[3] = vs.getPulse();
            row[4] = vs.isIsConcious(); // use is not get for boolean
            
            //add row
            model.addRow(row);
        }
        
        //select row to view information
        
    }
