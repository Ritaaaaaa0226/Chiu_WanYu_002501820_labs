    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        
        //select index value
        int selectedRow = tblVital.getSelectedRow();
        
        //validation
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(jScrollPane1, "Please select a row to delete ", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        //get the model, default table model,select which label
        DefaultTableModel model = (DefaultTableModel) tblVital.getModel();
        VitalSign selectedVital = (VitalSign) model.getValueAt(selectedRow, 0);//select Object not a String
        
        //delete 
        history.deleteVital(selectedVital);
        
        JOptionPane.showMessageDialog(jScrollPane1, "Vital Sign deleted ", "Successs", JOptionPane.WARNING_MESSAGE);
        
        //refresh table
        populateTable();
        
        //claer typed text
        fieldData.setText("");
        fieldTemperature.setText("");
        fieldBloodpressure.setText("");
        fieldPulse.setText("");
        lblStatus.setText("");
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        
        //select index value
        int selectedRow = tblVital.getSelectedRow();
        
        //validation
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(jScrollPane1, "Please select a row to view ", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        //get the model, default table model
        DefaultTableModel model = (DefaultTableModel) tblVital.getModel();
        VitalSign selectedVital = (VitalSign) model.getValueAt(selectedRow, 0);//select Object not a String
        
        if (selectedVital != null){
            
            fieldData.setText(selectedVital.getData());
            fieldTemperature.setText(String.valueOf(selectedVital.getTemperature()));//change type into string
            fieldBloodpressure.setText(String.valueOf(selectedVital.getBloodpressure()));
            fieldPulse.setText(String.valueOf(selectedVital.getPulse()));
            
            if(selectedVital.isIsConcious()){
                lblStatus.setText("Yes");
            }
            else{
                lblStatus.setText("No");
            }
          
            
        }
        
        
    }//GEN-LAST:event_btnViewActionPerformed


